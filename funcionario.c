void moduloFuncio(void) {
    char op;

    do {
        op = tela_menu_funcionarios();
        switch (op) {
            case '1':   tela_cadastro_funcionarios();
                        break;
            case '2':   tela_pesquisar_funcionarios();
                        break;
            case '3':   tela_alterar_funcionarios();
                        break;
            case '4':   tela_excluir_funcionarios();
                        break;
                
        }

    } while (op != '0');
}



